#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	char	c2;

	c2 = c;
	while (n > 0)
	{
		if (c2 == *(unsigned char *)s)
			return ((char *)s);
		s++;
		n--;
	}
	return (0);
}
